package sort;

public class PrintAnyArray {
    public void printAnyArray(int[] arr){
        System.out.println(" ");
        for(int i:arr){
            System.out.print(i+" ");
        }
    }

    public static void printAnyArrayStatic(int[] arr){
        System.out.println(" ");
        for(int i:arr){
            System.out.print(i+" ");
        }
    }
}
